char		*ft_strrev(char *str)
{
	int a;
	int x;
	int y;
	char swap;

	a = 0;
	while (str[a])
		a++;
	x = 0;
	y = a - 1;
	a = a / 2;
	while (a > 0)
	{
		swap = str[x];
		str[x] = str[y];
		str[y] = swap;
		a--;
		y--;
		x++;
	}
	return (str);
}
